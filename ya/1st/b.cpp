#include <iostream>

using namespace std;



int main() {
    char p[8][3][3] = {
            {{'3', '4', '5'}, {'2', '1', '6'}, {'9', '8', '7'}},
            {{'9', '8', '7'}, {'2', '1', '6'}, {'3', '4', '5'}},
            {{'5', '4', '3'}, {'6', '1', '2'}, {'7', '8', '9'}},
            {{'7', '8', '9'}, {'6', '1', '2'}, {'5', '4', '3'}},
            {{'3', '2', '9'}, {'4', '1', '8'}, {'5', '6', '7'}},
            {{'9', '2', '3'}, {'8', '1', '4'}, {'7', '6', '5'}},
            {{'7', '6', '5'}, {'8', '1', '4'}, {'9', '2', '3'}},
            {{'5', '6', '7'}, {'4', '1', '8'}, {'3', '2', '9'}},
    };
    string m[3];
    cin >> m[0];
    cin >> m[1];
    cin >> m[2];
    for(int i = 0; i < 8; i++){
        int ok = 0;
        for(int x = 0; x < 3; x++){
            for(int y = 0; y < 3; y++){
                if(p[i][x][y] == m[x][y]){
                    ok++;
                }
            }
        }
        if(ok == 3){
            for(int x = 0; x < 3; x++) {
                for (int y = 0; y < 3; y++) {
                    cout << p[i][x][y];
                }
                cout << endl;
            }
            return 0;
        }
    }
    return 0;
}