int main() {
	int c10, c50, c100, c500, c1000, c5000, p, cb, cm;
	scanf("%d %d %d %d %d %d", &c10, &c50, &c100, &c500, &c1000, &c5000);
	scanf("%d", &p);
	if (c10 > 0) {
		cb = (c10 * 10 + c50 * 50 + c100 * 100 + c500 * 500 + c1000 * 1000
				+ c5000 * 5000) / p;
		cm = ((c10 - 1) * 10 + c50 * 50 + c100 * 100 + c500 * 500 + c1000 * 1000
				+ c5000 * 5000) / p;
	} else if (c50 > 0) {
		cb = (c50 * 50 + c100 * 100 + c500 * 500 + c1000 * 1000 + c5000 * 5000)
				/ p;
		cm = ((c50 - 1) * 50 + c100 * 100 + c500 * 500 + c1000 * 1000
				+ c5000 * 5000) / p;
	} else if (c100 > 0) {
		cb = (c100 * 100 + c500 * 500 + c1000 * 1000 + c5000 * 5000) / p;
		cm = ((c100 - 1) * 100 + c500 * 500 + c1000 * 1000 + c5000 * 5000) / p;
	} else if (c500 > 0) {
		cb = (c500 * 500 + c1000 * 1000 + c5000 * 5000) / p;
		cm = ((c500 - 1) * 500 + c1000 * 1000 + c5000 * 5000) / p;
	} else if (c1000 > 0) {
		cb = (c1000 * 1000 + c5000 * 5000) / p;
		cm = ((c1000 - 1) * 1000 + c5000 * 5000) / p;
	} else if (c5000 > 0) {
		cb = (c5000 * 5000) / p;
		cm = ((c5000 - 1) * 5000) / p;
	}
	printf("%d\n", cb-cm);
	for(;cb>cm;cm++)
		printf("%d ", cm+1);
	return 0;
}


